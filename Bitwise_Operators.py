#Bitwise Operators
a=int(input("Enter first number:- "))
b=int(input("Enter second number:- "))
print("AND OPERATION(a & b):- ",a & b)
print("OR OPERATION(a | b):- ",a | b)
print("NOT OPERATION(~a):- ",~a)
print("XOR OPERATION(a ^ b):- ",a ^ b)
print("RIGHT SHIFT OPERATION(a >> 1):- ",a >> 1)
print("LEFT SHIFT OPERATION(a << 1):- ",a << 1)
# OUTPUT:-
# Enter first number:- 10
# Enter second number:- 2
# AND OPERATION(a & b):-  2
# OR OPERATION(a | b):-  10
# NOT OPERATION(~a):-  -11
# XOR OPERATION(a ^ b):-  8
# RIGHT SHIFT OPERATION(a ^ b):-  5
# LEFT SHIFT OPERATION(a ^ b):-  20